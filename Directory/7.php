<h1>Lucky Girl (2019)</h1>
<p>Year 2008, Pennsylvania. Esther, a 9-year-old Chinese adoptee just moved to a new neighborhood with her family. On her Gotcha day, she realizes that the Chinese buffet they are going to might not have "real Chinese food."</p>
